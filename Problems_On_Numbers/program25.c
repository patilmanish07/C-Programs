#include<stdio.h>
#include<stdbool.h>
int CheckEven(int);

////////////////////////////////////////////////////////////////////////////////////////////////////////
// Helper.c
//////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function Name     :-    CheckEven
//	Input 			  :-    Integer value. 	
//	Output			  :-	Boolean Value(true/false)
//	Description       :-    This Function is used to check number is even or odd.
//  Author 			  :-    Manish Bhuepndra Patil	
//  Date 			  :-    1/07/2022
////////////////////////////////////////////////////////////////////////////////////////////////////////////////

 int CheckEven(int iNo)
 {
    if((iNo % 2)== 0)
	{
		return 1;
	}
    else
	{
		return 0;
    }
 }	


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Main.c
 int main()
{
	int iValue = 0;
	int iRet = 0;
	
	
	printf("Enter number\n");
	scanf("%d",&iValue);
	
	iRet = CheckEven(iValue);
	
	if(iRet == 1)
	{
	    printf("%d is even number\n",iValue);
	}
	else
	{
		printf("%d is odd number\n",iValue);
    }		
		
	return 0;
}	