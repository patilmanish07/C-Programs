void DisplayEven(int iNo)            //Defination													
{
	int iDigit = 0;
	
	if(iNo < 0)
	{
		iNo = -iNo;
	}	
	
	while(iNo > 0)
	{
		iDigit = iNo % 10;
		if((iDigit % 2) == 0)
        {
			printf("%d\n",iDigit);
        }
		iNo = iNo / 10;
    }
}	
	

