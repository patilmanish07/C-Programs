// Main.c
int main()
{
	int iValue = 0;
	int iRet = 0;
	printf("Enter number\n");
	scanf("%d",&iValue);
	
	iRet = Reverse(iValue);

    printf("Reverse number is :%d\n",iRet);
	
	return 0;
}	

