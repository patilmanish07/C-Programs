int Reverse(int iNo)
{
	int iDigit = 0;
	int iRev = 0;
	
	while(iNo > 0)
	{
		iDigit = iNo % 10;
        iRev = iRev * 10 + iDigit;          
        iNo = iNo / 10;
    }
     return iRev;	
}	

